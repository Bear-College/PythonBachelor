# неявні перетворення 

a1 = 4.7
b1 = 2   
c1 = a1 + b1 

print(c1)

# явні перетворення

# перетворення для int 
a2 = 2
b2 = "5"
c2 = 4.7
d2 = a2 + int(b2) + int(c2) 

print(d2)

# перетворення для float

a3 = 7
b3 = "2"
c3 = 1.3
d3 = float(a3) + float(b3) + c3

print(d3)

# перетворення для str 

kil = 5 
massage = "Кількість присутніх студентів: " + str(kil)

print(massage)