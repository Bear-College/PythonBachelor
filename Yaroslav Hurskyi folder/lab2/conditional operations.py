a = 5
b = 12

# a дорівнює b
if a == b:
    print(True)
else:
    print(False)

# a не дорівнює b
if a != b:
    print(True)
else:
    print(False)

# a більше b
if a > b:
    print(True)
else:
    print(False)

# a менше b
if a < b:
    print(True)
else:
    print(False)

# a більше або дорівнює b
if a >= b:
    print(True)
else:
    print(False)

# a менше фбо дорівнює b
if a <= b:
    print(True)
else:
    print(False)